public class switch_case_test {
    public static void main(String[] args) {

        int floor = 3;

        switch (floor) {
            case 1:
            System.out.println("약국 입니다");
            break;
            case 2:
                System.out.println("정형외과 입니다");
                break;
            case 3:
                System.out.println("피부과 입니다");
                break;
            case 4:
                System.out.println("치과 입니다");
                break;
            case 5:
                System.out.println("헬스 클럽 입니다");
                break;
        }
    }
}
