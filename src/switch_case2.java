public class switch_case2 {

    public static void main(String[] args) {
        int mon = 3;
        int day;
        switch (mon) {
            case 1:
                day = 31;
                break;
            case 2:
                day = 28;
                break;
            case 3:
                day = 31;
                break;
            case 4:
                day = 30;
                break;
            case 5:
                day = 31;
                break;

        }
    }
}
